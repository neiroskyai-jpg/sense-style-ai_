"""Подбор предметного фото к названию вещи из библиотеки `web/photos/items/`.

Названия вещей приходят из генерации свободным текстом («жакет полуприлегающего силуэта
серо-синего цвета», «ботильоны молочного цвета на устойчивом каблуке»). Библиотека собрана по
ТИПАМ вещей, поэтому здесь мы вытаскиваем тип из названия и отдаём путь к готовому кадру.

Генерации на этом пути нет: библиотека сделана один раз (см. scripts/gen_item_images.py), показ
бесплатный. Не нашли тип — честно отдаём пустоту, и карточка рисует подпись вместо картинки.
"""
from __future__ import annotations

import re
from pathlib import Path

ITEMS_DIR = Path(__file__).resolve().parent.parent / "web" / "photos" / "items"
URL_PREFIX = "/photos/items/"

# Тип вещи → корни слов, по которым он узнаётся в названии. Порядок важен: более узкие типы
# идут раньше общих, иначе «джинсовая юбка» уедет в джинсы, а не в юбку.
_TYPE_ROOTS: list[tuple[str, tuple[str, ...]]] = [
    ("тренч", ("тренч",)),
    ("плащ", ("плащ",)),
    ("пальто", ("пальто",)),
    ("жакет", ("жакет", "пиджак", "блейзер")),
    ("водолазка", ("водолазк", "гольф")),
    ("джемпер", ("джемпер", "свитер", "пуловер", "кардиган", "лонгслив")),
    ("блуза", ("блуз", "топ ", "топы", "боди")),
    ("рубашка", ("рубашк", "сорочк")),
    ("футболка", ("футболк", "майка")),
    ("платье", ("платье", "сарафан", "комбинезон")),
    ("юбка", ("юбк",)),
    ("джинсы", ("джинс",)),
    ("брюки", ("брюк", "штан", "кюлот")),
    ("ботильоны", ("ботильон", "ботинк")),
    ("сапоги", ("сапог", "ботфорт")),
    ("лоферы", ("лофер", "мокасин", "балетк", "оксфорд")),
    ("туфли", ("туфл", "лодочк", "слингбэк", "босонож")),
    ("сумка", ("сумк", "клатч", "шопер", "тоут")),
    ("ремень", ("ремен", "ремешок", "пояс")),
    ("шарф", ("шарф", "платок", "палантин", "снуд")),
]


def item_type(name: str) -> str:
    """Тип вещи по свободному названию. Пусто — не распознали."""
    low = " " + re.sub(r"\s+", " ", (name or "").lower()) + " "
    for kind, roots in _TYPE_ROOTS:
        if any(root in low for root in roots):
            return kind
    return ""


def item_image_url(name: str) -> str:
    """URL готового кадра для вещи. Пусто — типа нет в библиотеке или файл не сгенерирован."""
    kind = item_type(name)
    if not kind:
        return ""
    return f"{URL_PREFIX}{kind}.jpg" if (ITEMS_DIR / f"{kind}.jpg").exists() else ""


def available_types() -> set[str]:
    """Типы, для которых кадр реально лежит на диске (для тестов и диагностики)."""
    if not ITEMS_DIR.is_dir():
        return set()
    return {p.stem for p in ITEMS_DIR.glob("*.jpg")}
