def fit_rules(figure_type: str | None) -> dict | None:
    """Полный ruleset по коду фигуры (или None, если код неизвестен)."""
    return FIGURE_FIT_RULES.get(figure_type or "")


def fit_rules_prompt(figure_type: str | None) -> str:
    """Компактный текст правил для грунтовки генерации капсулы. Пусто — если фигура неизвестна."""
    r = fit_rules(figure_type)
    if not r:
        return ""
    combos = "; ".join(r["silhouettes"])
    return (
        f"ПРАВИЛА ПОСАДКИ ПОД ФИГУРУ ({r['label']}) — соблюдай строго при выборе вещей капсулы:\n"
        f"- Подчёркиваем: {r['emphasize']}.\n"
        f"- Баланс: {r['balance']}.\n"
        f"- Посадка и размеры: {r['fit']}.\n"
        f"- Безошибочные силуэты (бери отсюда): {combos}.\n"
        f"- Избегать: {r['avoid']}."
    )


def fit_rules_client(figure_type: str | None) -> dict | None:
    """Короткая версия для показа клиентке в Карте (что подчёркиваем/посадка/силуэты)."""
    r = fit_rules(figure_type)
    if not r:
        return None
    return {
        "label": r["label"],
        "emphasize": r["emphasize"],
        "balance": r["balance"],
        "fit": r["fit"],
        "silhouettes": r["silhouettes"],
        "avoid": r["avoid"],
    }
