def _profile_block(profile: dict | None) -> str:
    """Контекст клиентки: её Формула + сработавшие правила базы (RAG), если есть."""
    diag = (profile or {}).get("diagnosis") or {}
    if not diag.get("style_formula"):
        return ("\n\nУ клиентки пока НЕТ Формулы стиля (не прошла диагностику). "
                "Деликатно подведи её пройти бесплатную диагностику.")
    lines = [f"\n\nФОРМУЛА КЛИЕНТКИ (опирайся на неё в советах):",
             f"- Формула: {diag.get('style_formula')}",
             f"- Цветотип: {diag.get('colortype')}",
             f"- Фигура: {diag.get('figure_type')}"]
    # Gap может быть None, когда распределения негодны (см. _recompute_gap): «None%» в подсказке
    # стилисту — мусор, лучше строку опустить, чем печатать несуществующее число.
    if isinstance(diag.get("gap_percentage"), (int, float)):
        lines.append(f"- Identity Gap: {diag['gap_percentage']}%")
    vf = diag.get("visual_formula") or {}
    if vf.get("stop_list"):
        lines.append("- Стоп-лист: " + ", ".join(vf["stop_list"][:5]))
    deep = diag.get("deep_intake") or {}  # тело+возражения из анкеты Карты
    if deep.get("adv"):
        lines.append(f"- Подчёркиваем достоинство: {deep['adv']}")
    if deep.get("balance"):
        lines.append(f"- Хочет уравновесить: {deep['balance']}")
    if deep.get("taboo"):
        lines.append(f"- Личные табу (не предлагать): {deep['taboo']}")
    if deep.get("audience"):
        lines.append(f"- Учитывает мнение: {deep['audience']} — но веди к запросу про СЕБЯ")
    portrait = (deep.get("personality") or {}).get("portrait") or diag.get("personality_portrait")
    if portrait:
        lines.append(f"- Её натура (личность): {portrait}")
    if rag is not None:
        try:
            rules = rag.retrieve({
                "colortype": diag.get("colortype"), "figure_type": diag.get("figure_type"),
                "style_formula": diag.get("style_formula"),
                "semantic_field_distribution": diag.get("semantic_field_distribution"),
            }, k=4)
            if rules:
                lines.append("\nПРАВИЛА БАЗЫ ПОД ЕЁ ПРОФИЛЬ (используй как опору):")
                lines += [f"- [{r['source_label']}] {r['section']}: {r['text'][:200]}" for r in rules]
        except Exception:  # noqa: BLE001
            pass
    return "\n".join(lines)


def stylist_reply(history: list[dict], profile: dict | None = None,
                  mode: str | None = None) -> str:
    """history — [{role:'user'|'assistant', content:str}, …]. Вернёт ответ стилиста."""
    system = _BASE + _profile_block(profile)
    msgs = [{"role": "system", "content": system}]
    # последние ходы диалога (ограничиваем, чтобы не раздувать контекст)
    for m in (history or [])[-12:]:
        role = "assistant" if m.get("role") == "assistant" else "user"
        text = (m.get("content") or "").strip()[:1500]
        if text:
            msgs.append({"role": role, "content": text})
    return provider.chat_messages(config.model_for("text", mode), msgs, max_tokens=600)
