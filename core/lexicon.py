"""Лексикон впечатления — единственный источник правды о привязке слов к полям.

Зачем отдельный модуль: до него привязка жила в трёх местах — списком `wantField` в
`web/identity-scan-quiz.html`, текстом в `architecture/prompts/formula-diagnostic.md` и никак
в Python. Точку А короткий квиз собирал вообще словами вне лексикона («спокойная»,
«незаметная»), поэтому `now_field_distribution` не строился, `_recompute_gap` выходил до
расчёта — и Identity Gap приходил с клиента JS-эвристикой. На 922 сохранённых прогонах это
дало 910 одинаковых 31% и 813 «классиков»: шкала не мерила, а отдавала константу.

Источник: анкета школы «Алгоритмы имиджа», вопросы 4 («какое впечатление производите сейчас»)
и 5 («какое хотели бы после изменения»). Оба вопроса используют ОДИН список — это принципиально:
обе точки меряются одной линейкой, иначе разрыв между ними не имеет смысла. Сверено с
оригиналом PDF 23.07.2026: ровно 66 слов, 15/16/17/18 по полям. «80» в заголовке метода —
ошибка, реальное число 66.

Жёсткое правило метода: список калиброван, слова «вне системы» не добавляются. Каждое новое
слово — только явно в поле и только с сохранением баланса.
"""
from __future__ import annotations

# Поля метода. Порядок фиксирован: он же в _GAP_FIELDS пайплайна и в JSON-контракте промпта.
FIELDS = ("natural", "romance", "drama", "classic")

# 66 слов анкеты. Ключ — в нижнем регистре и через «е» вместо «ё»: клиентка пишет как придётся,
# а сравнение должно совпадать. Нормализация — в _norm.
LEXICON: dict[str, str] = {}


def _fill(field: str, words: str) -> None:
    for w in words.split("·"):
        w = _norm(w)
        if w:
            LEXICON[w] = field


def _norm(word: str) -> str:
    """Ключ словаря: без регистра, без ё, без крайних пробелов.

    «Веселая» и «Весёлая» — одно слово; анкета печатает без ё, лексикон проекта с ё.
    """
    return (word or "").strip().lower().replace("ё", "е")


_fill("natural", """
    Деловитая · Независимая · Простая · Солидарная · Доверительная · Открытая ·
    Коммуникабельная · Беззаботная · Непритязательная · Толерантная · Непосредственная ·
    Располагающая · Экологичная · Весёлая · Любознательная
""")

_fill("romance", """
    Привлекательная · Женственная · Романтическая · Очаровательная · Обольстительная ·
    Легкомысленная · Желанная · Роковая · Эффектная · Импозантная · Несравненная · Шикарная ·
    Чувствительная · Нежная · Ранимая · Загадочная
""")

_fill("drama", """
    Смелая · Неординарная · Эксцентричная · Провокационная · Умная · Новая · Незаурядная ·
    Эпатирующая · Одинокая · Странная · Особенная · Прячущая · Демонстрирующая ·
    Отстранённая · Вдумчивая · Замкнутая · Ищущая
""")

_fill("classic", """
    Организованная · Собранная · Сдержанная · Благородная · Притязательная · Дорогая ·
    Превосходная · Надёжная · Авторитарная · Авторитетная · Компетентная · Честолюбивая ·
    Уверенная · Элегантная · Солидная · Серьёзная · Властная · Амбициозная
""")

# Написания, которые встречаются в живых ответах, но не входят в счёт 66. Держим отдельно,
# чтобы инвариант «66 слов, 15/16/17/18 по полям» оставался проверяемым тестом.
# «Эпатирующий» — мужской род в оригинале PDF, опечатка первоисточника.
ALIASES: dict[str, str] = {_norm("Эпатирующий"): "drama"}

# Короткий квиз (Identity Scan) берёт выборку 12 слов: 4 поля × 3 вектора, баланс обязателен.
# Тот же набор используется и для точки А, и для точки Б — как вопросы 4 и 5 бумажной анкеты.
SHORT_SET: tuple[str, ...] = (
    "Властная", "Элегантная", "Дорогая",            # classic: власть · утончённость · статус
    "Незаурядная", "Смелая", "Умная",               # drama: индивидуальность · дерзость · интеллект
    "Женственная", "Эффектная", "Желанная",         # romance: мягкость · яркость · притяжение
    "Открытая", "Беззаботная", "Независимая",       # natural: контакт · лёгкость · самостоятельность
)


def field_of(word: str) -> str | None:
    """Поле слова или None, если слова нет в калиброванном списке."""
    key = _norm(word)
    return LEXICON.get(key) or ALIASES.get(key)


def distribution(words) -> dict[str, float] | None:
    """Слова → распределение по 4 полям в процентах, сумма 100.

    Возвращает None, если ни одно слово не опознано: пустое распределение хуже отсутствия —
    на нём `_recompute_gap` посчитал бы разрыв по нулям и выдал бы уверенное число из ничего.

    Слова вне лексикона игнорируются молча по числу, но их количество отдаёт `unknown_words` —
    вызывающий решает, считать ли замер полноценным.
    """
    known = [f for f in (field_of(w) for w in (words or [])) if f]
    if not known:
        return None
    share = 100.0 / len(known)
    dist = {f: 0.0 for f in FIELDS}
    for f in known:
        dist[f] += share
    # хвост округления кладём в самое весомое поле, чтобы сумма была ровно 100
    dist = {f: round(v, 1) for f, v in dist.items()}
    diff = round(100.0 - sum(dist.values()), 1)
    if diff:
        top = max(FIELDS, key=lambda f: dist[f])
        dist[top] = round(dist[top] + diff, 1)
    return dist


def unknown_words(words) -> list[str]:
    """Слова, которых нет в лексиконе. Пустой список — замер сделан целиком по методу."""
    return [w for w in (words or []) if not field_of(w)]


def dominant(dist: dict) -> str | None:
    """Поле с наибольшей долей. При равенстве — по порядку FIELDS, чтобы результат был
    воспроизводим: иначе один и тот же ввод давал бы разную доминанту от запуска к запуску."""
    if not isinstance(dist, dict):
        return None
    best = max(FIELDS, key=lambda f: (float(dist.get(f) or 0), -FIELDS.index(f)))
    return best if float(dist.get(best) or 0) > 0 else None
