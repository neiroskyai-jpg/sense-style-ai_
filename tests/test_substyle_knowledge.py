"""Генератор образов обязан ЗНАТЬ, что означает подстиль, а не только его название.

Реальный случай (17.07.2026): клиентке с формулой «Леди-лайк × Soft Classic» собрали образ с
фланелевой рубашкой в клетку. Причина — в промпт попадал только ярлык: в style-library леди-лайк
упомянут лишь в таблице соответствий («→ №17»), а описание (платья приталенного силуэта, миди,
перчатки, эстетика 50-60-х, Одри Хепберн) лежало в методе и не подклеивалось. Ярлык без
содержания модель наполняет как умеет.
"""
import os

os.environ.setdefault("OPENROUTER_API_KEY", "dummy")

from core.pipeline import _substyles_reference  # noqa: E402


def test_podstili_zagruzheny_s_opisaniyami():
    s = _substyles_reference()
    assert len(s) > 3000, "раздел подстилей подозрительно мал — метод переструктурировали?"
    assert "Маркеры" in s and "Прототипы" in s, "нужны маркеры и прототипы, а не список названий"


def test_ledi_layk_opisan_konkretno():
    """Тот самый подстиль, на котором сломались: должен нести вещи и эталоны, а не имя."""
    s = _substyles_reference().lower()
    assert "леди-лайк" in s
    assert "приталенн" in s and "перчатки" in s, "нет вещей — модель снова додумает фланель"
    assert "одри хепберн" in s or "грейс келли" in s, "нет прототипов — не на что опереться"


def test_vse_chetyre_polya_predstavleny():
    """Все базовые стили: клиентка может прийти с любой доминантой."""
    s = _substyles_reference().lower()
    for field in ("классик", "драм", "романтик", "натуральн"):
        assert field in s, f"в справке подстилей нет поля {field}"


def test_bez_manifesta_i_teorii():
    """Тянем раздел, а не метод целиком: промпт и так ~9 тыс. токенов."""
    s = _substyles_reference()
    assert "Манифест метода" not in s
    assert len(s) < 15000, "в промпт уехало больше, чем раздел подстилей"
