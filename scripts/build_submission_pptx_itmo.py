def set_text(shape, text: str) -> None:
    """Заменить текст фигуры, сохранив оформление первого прогона.

    Наивное `shape.text_frame.text = ...` сбрасывает шрифт и кегль на дефолтные — на фирменном
    шаблоне это заметно сразу. Поэтому пишем в первый run, лишние run'ы и абзацы убираем.
    """
    tf = shape.text_frame
    paragraphs = tf.paragraphs
    first = paragraphs[0]
    if first.runs:
        first.runs[0].text = text
        for extra in first.runs[1:]:
            extra._r.getparent().remove(extra._r)
    else:
        first.text = text
    for extra in list(paragraphs[1:]):
        extra._p.getparent().remove(extra._p)


def norm(s: str) -> str:
    """Шаблон местами содержит неразрывные пробелы и мягкие переносы — сравниваем по сути."""
    return " ".join(s.replace("​", "").replace("\xa0", " ").split())


def main() -> int:
    if not TEMPLATE.exists():
        print(f"Не нашёл шаблон: {TEMPLATE}")
        return 1

    prs = Presentation(str(TEMPLATE))
    sldIdLst = prs.slides._sldIdLst
    ids = list(sldIdLst)

    # Сначала заполняем текст (индексы ещё соответствуют исходному шаблону), потом переставляем.
    for tpl_idx, replacements in CONTENT.items():
        slide = prs.slides[tpl_idx]
        wanted = {norm(k): v for k, v in replacements.items()}
        for shape in slide.shapes:
            if not shape.has_text_frame:
                continue
            key = norm(shape.text_frame.text)
            if key in wanted:
                set_text(shape, wanted.pop(key))
        if wanted:
            print(f"  ! слайд {tpl_idx + 1}: не нашёл блоки — {list(wanted)[:3]}")

    # Тексты-описания: берём фигуры-«рыбу» и раскладываем по позиции (сверху вниз, слева направо).
    filler = ("Крупнейшая онлайн-магистратура", "Сообщество экспертов", "Подготовка на реальных")
    for tpl_idx in {k[0] for k in BODIES}:
        slide = prs.slides[tpl_idx]
        bodies = [s for s in slide.shapes
                  if s.has_text_frame and norm(s.text_frame.text).startswith(filler)]
        bodies.sort(key=lambda s: (round(s.top / 914400, 1), s.left))
        for order, shape in enumerate(bodies):
            if (tpl_idx, order) in BODIES:
                set_text(shape, BODIES[(tpl_idx, order)])

    # Год в углу — шаблон нарисован для 2024.
    for slide in prs.slides:
        for shape in slide.shapes:
            if shape.has_text_frame and norm(shape.text_frame.text) == "2024":
                set_text(shape, "2026")

    # Переставляем в нужном порядке и выбрасываем лишние демо-слайды.
    for sld_id in ids:
        sldIdLst.remove(sld_id)
    for tpl_idx in KEEP:
        sldIdLst.append(ids[tpl_idx])

    DEST.parent.mkdir(parents=True, exist_ok=True)
    prs.save(str(DEST))
    print(f"OK: {DEST.relative_to(ROOT)} — {len(KEEP)} слайдов")
    print("Дальше вручную в PowerPoint: вставить фото в плейсхолдеры «фото», "
          "проверить, что текст не вылезает, сохранить как PDF.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
